def угадай_число():
  тайное_число = random.randint(1, 100)
  попытки = 0
  while попытки < 7:
    try:
      число = int(input("Угадайте число (1-100): "))
      попытки += 1
      if число < тайное_число:
        print("Слишком мало!")
      elif число > тайное_число:
        print("Слишком много!")
      else:
        print(f"Вы угадали за {попытки} попыток!")
        return
    except ValueError:
      print("Неверный ввод. Введите число.")
  print(f"Вы не угадали. Число было {тайное_число}.")

угадай_число()